		if table.HasValue( Drem_CCH_models, npc:GetModel() ) then npc:SetSkin( math.random(0,2) );
		end
end)

hook.Add( "PlayerSpawnedNPC", "RandomBodygroup", function(ply,npc)
		if table.HasValue( Drem_CCH_models, npc:GetModel() ) then npc:SetBodygroup( 1, math.random(1,12) );
		npc:SetBodygroup( 2, math.random(0,3) );
		npc:SetBodygroup( 4, math.random(0,7) );
		end
end)
